XT = [16; -4+4j; -4; -4-4j];			% Ορισμός Πίνακα XT[k]
W4 = [1,1,1,1; 1,-j,-1,j; 1,-1,1,-1; 1,j,-1,j]; % Ορισμός Πίνακα W4
W4_inv = inv(W4)				% Εύρεση αντίστροφου Πίνακα W4^-1
xT = W4_inv * XT;				% Εύρεση xT[n] από σχέση (12.27)
x = xT'						% Εύρεση x[n] με αναστροφή Πίνακα xT