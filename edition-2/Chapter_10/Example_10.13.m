% Example 10.13
clear all; clc;

% Ορισμός ακολουθιών x1[n] και x2[n]
x1 = [0, 1, 2, 3]; x2 = [1, 2, -1, 0];

% Υπολογισμός γραμμικής συνέλιξης
y1 = conv(x1, x2)

% Μήκος κυκλικής συνέλιξης 
N = 4;

% Υπολογισμός DFT Ν-σημείων X1[k] και X2[k]
X1 = fft(x1, N); X2 = fft(x2, N);
 
% Πολλαπλασιασμός Υ[k] = X1[k].X2[k]
Y2 = X1 .* X2;
 
% Υπολογισμός αντίστροφου DFT
y2 = ifft(Y2, N)
