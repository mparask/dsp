% Example 10.12

% Μήκος κυκλικής συνέλιξης 
N = 4;
 
% Ορισμός κλίμακας χρόνου και ακολουθιών x[n] και g[n]
n = [0, 1, 2, 3]; x = [0, 1, 2, 3]; g = [1, 2, 0, -1];
 
% Υπολογισμός DFT Ν-σημείων X[k] και G[k]
X = fft(x, N); G = fft(g, N);
 
% Πολλαπλασιασμός Υ[k] = X[k].G[k]
Y = X .* G;
 
% Υπολογισμός αντίστροφου DFT
y = ifft(Y, N);
 
% Κυκλική μετατόπιση κατά -1
y = cirshftt(y,-1,N)
